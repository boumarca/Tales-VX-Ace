#==============================================================================
# ** New Class
# ** Game_SkillShortcut
#------------------------------------------------------------------------------
#  This is a class representing a pair Actor, Skill representing an 
# Arte Shortcut.
#==============================================================================

class Game_SkillShortcut
  #--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize
    clear
  end
  #--------------------------------------------------------------------------
  # * Get Actor
  #--------------------------------------------------------------------------
  def actor
    $data_actors[@actor_id]
  end
  #--------------------------------------------------------------------------
  # * Get Skill
  #--------------------------------------------------------------------------
  def skill
    $data_skills[@skill_id]
  end
  #--------------------------------------------------------------------------
  # * Set Shortcut
  #--------------------------------------------------------------------------
  def shortcut(actor, skill)
    @actor_id = actor.id
    @skill_id = skill.id
  end
  #--------------------------------------------------------------------------
  # * Clear Shortcut
  #--------------------------------------------------------------------------
  def clear
    @actor_id = 0
    @skill_id = 0
  end
  #--------------------------------------------------------------------------
  # * Is an empty shortcut
  #--------------------------------------------------------------------------
  def empty?
    @actor_id == 0 && @skill_id == 0
  end
end