# ** New Class
# ** Game_CookingShortcut
#------------------------------------------------------------------------------
#  This is a class representing a pair Actor, Skill representing an 
# Arte Shortcut.
#==============================================================================

class Game_CookingShortcut
  #--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize
    clear
  end
  #--------------------------------------------------------------------------
  # * Get Actor
  #--------------------------------------------------------------------------
  def actor
    $data_actors[@actor_id]
  end
  #--------------------------------------------------------------------------
  # * Get Recipe
  #--------------------------------------------------------------------------
  def recipe
    $data_recipes[@recipe_id]
  end
  #--------------------------------------------------------------------------
  # * Set Shortcut
  #--------------------------------------------------------------------------
  def shortcut(actor, recipe)
    @actor_id = actor.id
    @recipe_id = recipe.id
  end
  #--------------------------------------------------------------------------
  # * Clear Shortcut
  #--------------------------------------------------------------------------
  def clear
    @actor_id = 0
    @recipe_id = 0
  end
  #--------------------------------------------------------------------------
  # * Is an empty shortcut
  #--------------------------------------------------------------------------
  def empty?
    @actor_id == 0 && @recipe_id == 0
  end
end