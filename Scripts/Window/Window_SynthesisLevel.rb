#==============================================================================
# ** New Class
# ** Window_SynthesisLevel**
#------------------------------------------------------------------------------
#  This window displays the crafting level.
#==============================================================================

class Window_SynthesisLevel < Window_Base
  #--------------------------------------------------------------------------
  # * Constants (Positions)
  #--------------------------------------------------------------------------
  WINDOW_WIDTH    = 304
  #--------------------------------------------------------------------------
  # * Modified
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize(x, y)
    super(x, y, window_width, window_height) 
    draw_level
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Get Window Width
  #--------------------------------------------------------------------------
  def window_width
    return WINDOW_WIDTH
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Get Window Width
  #--------------------------------------------------------------------------
  def window_height
    fitting_height(1)
  end
  #--------------------------------------------------------------------------
  # * Draw Synthesis Level
  #--------------------------------------------------------------------------
  def draw_level
    str = sprintf("%s : %d", Vocab.level, $game_party.synthesis_level)
    draw_text(0, 0, contents.width, line_height, str, Bitmap::ALIGN_CENTER)
  end
  #--------------------------------------------------------------------------
  # * Refresh
  #--------------------------------------------------------------------------
  def refresh
    contents.clear
    draw_level
  end
end