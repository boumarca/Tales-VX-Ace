#==============================================================================
# ** New Class
# ** Window_SimpleHelp
#------------------------------------------------------------------------------
#  Main parent class of the help window with simple description.
#==============================================================================

class Window_SimpleHelp < Window_Help
  #--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize(x, y)
    super(4, Graphics.width - 32)
    self.x = x
    self.y = y
    clear
    refresh
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Set Content Name
  #--------------------------------------------------------------------------
  def set_name(name)
    if name != @name
      @name = name
    end
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Set Item
  #     item : Skills and items etc.
  #--------------------------------------------------------------------------
  def set_item(item)
    set_name(item ? item.name : "")
    super
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Clear
  #--------------------------------------------------------------------------
  def clear
    set_name("")
    super
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Refresh
  #--------------------------------------------------------------------------
  def refresh
    contents.clear
    draw_item_name
    draw_item_description
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Draw Item Name
  #--------------------------------------------------------------------------
  def draw_item_name
    draw_text(0, 0, contents.width, line_height, @name)
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Draw Main Description
  #--------------------------------------------------------------------------
  def draw_item_description
    draw_text_ex(10, line_height, @text)
  end
end