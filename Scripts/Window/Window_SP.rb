#==============================================================================
# ** Window_SP
#------------------------------------------------------------------------------
#  This window shows the remaining SP for the current actor.
#==============================================================================

class Window_SP < Window_Base
  #--------------------------------------------------------------------------
  # * Constants (Positions)
  #--------------------------------------------------------------------------
  WINDOW_WIDTH  = 256
  WINDOW_HEIGHT = 60
  GAUGE_X       = 26
  GAUGE_Y       = 6
  GAUGE_WIDTH   = 180
  #--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize(x, y)
    super(x, y, window_width, window_height)
    @actor = nil
    refresh
  end
  #--------------------------------------------------------------------------
  # * Get Window Width
  #--------------------------------------------------------------------------
  def window_width
    return WINDOW_WIDTH
  end
  #--------------------------------------------------------------------------
  # * Get Window Height
  #--------------------------------------------------------------------------
  def window_height
    return WINDOW_HEIGHT
  end
  #--------------------------------------------------------------------------
  # * Actor Settings
  #--------------------------------------------------------------------------
  def actor=(actor)
    return if @actor == actor
    @actor = actor
    refresh
  end
  #--------------------------------------------------------------------------
  # * Refresh
  #--------------------------------------------------------------------------
  def refresh
    contents.clear
    draw_actor_sp(@actor, GAUGE_X, GAUGE_Y, GAUGE_WIDTH) if @actor
  end
end