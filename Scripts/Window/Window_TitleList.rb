#==============================================================================
# ** New Class
# ** Window_TitleList
#------------------------------------------------------------------------------
#  This window is for choosing which title to equip.
#==============================================================================

class Window_TitleList < Window_Selectable
  #--------------------------------------------------------------------------
  # * Constants (Positions)
  #--------------------------------------------------------------------------
  WINDOW_WIDTH = 256
  #--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize(x, y)
    super(x, y, window_width, window_height)
    @data = []
    @actor = nil
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Get Window Width
  #--------------------------------------------------------------------------
  def window_width
    return WINDOW_WIDTH
  end
  #--------------------------------------------------------------------------
  # * Modified
  # * Get Window Height
  #--------------------------------------------------------------------------
  def window_height
    fitting_height(11)
  end
  #--------------------------------------------------------------------------
  # * Get Number of Items
  #--------------------------------------------------------------------------
  def item_max
    @data ? @data.size : 1
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Draw Item
  #--------------------------------------------------------------------------
  def draw_item(index)
    title = @data[index]
    if title
      rect = item_rect_for_text(index)
      rect.width -= 4
      draw_title_name(title, rect)
    end
  end
  #--------------------------------------------------------------------------
  # * Draw Title Name
  #--------------------------------------------------------------------------
  def draw_title_name(title, rect)
    draw_text(rect, title.name)
  end
  #--------------------------------------------------------------------------
  # * Set Actor
  #--------------------------------------------------------------------------
  def actor=(actor)
    return if @actor == actor
    @actor = actor
    refresh
  end
  #--------------------------------------------------------------------------
  # * Get Title
  #--------------------------------------------------------------------------
  def title
    @data && index >= 0 ? @data[index] : nil
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Update Help Text
  #--------------------------------------------------------------------------
  def update_help
    @help_window.set_item(title)
  end
  #--------------------------------------------------------------------------
  # * Create Title List
  #--------------------------------------------------------------------------
  def make_title_list
    @data = @actor.titles
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Restore Previous Selection Position
  #--------------------------------------------------------------------------
  def select_last
    select(@data.index(@actor.current_title) || 0)
  end
  #--------------------------------------------------------------------------
  # * Override
  #* Refresh
  #--------------------------------------------------------------------------
  def refresh
    make_title_list
    super
  end
end