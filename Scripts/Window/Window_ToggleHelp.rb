#==============================================================================
# ** New Class
# ** Window_ToggleHelp
#------------------------------------------------------------------------------
#  Main parent class of the help window with toggleable description.
#==============================================================================

class Window_ToggleHelp < Window_SimpleHelp
  #--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize(x, y)
    @show_alt_text = false
    super
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Clear
  #--------------------------------------------------------------------------
  def clear
    set_alt_text("")
    super
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Set Alternate Text
  #--------------------------------------------------------------------------
  def set_alt_text(alt_text)
    if alt_text != @alt_text
      @alt_text = alt_text
    end
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Toggle Alternate Text
  #--------------------------------------------------------------------------
  def toggle_text
    @show_alt_text = !@show_alt_text
    refresh
  end
  #--------------------------------------------------------------------------
  # * Override
  # * Refresh
  #--------------------------------------------------------------------------
  def refresh
    contents.clear
    draw_item_name
    draw_main if !@show_alt_text
    draw_toggle if @show_alt_text
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Draw Main Help
  #--------------------------------------------------------------------------
  def draw_main
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Draw Toggle Help
  #--------------------------------------------------------------------------
  def draw_toggle
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Draw Alternate Description
  #--------------------------------------------------------------------------
  def draw_alt_description
    draw_text_ex(10, line_height, @alt_text)
  end
end