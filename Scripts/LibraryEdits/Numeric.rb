class Numeric  
  #--------------------------------------------------------------------------
  # * New Method
  # * Formats numbers into D,DDD,DDD
  # * Source :Yanfly
  #--------------------------------------------------------------------------
  def group
    self.to_s.gsub(/(\d)(?=\d{3}+(?:\.|$))(\d{3}\..*)?/,'\1,\2')
  end
  #--------------------------------------------------------------------------
  # * New Method
  # * Clamp self between min and max
  #--------------------------------------------------------------------------
  def clamp(min, max)
    [[self, max].min, min].max
  end
end 